def depression_test():
    questions = [
        "1. 최근 2주 동안, 거의 매일 우울하거나 희망이 없다고 느꼈습니까?",
        "2. 최근 2주 동안, 거의 매일 흥미나 즐거움을 잃은 적이 있습니까?",
        "3. 최근 2주 동안, 거의 매일 수면 문제가 있었습니까? (잠들기 어렵거나 너무 많이 잠)",
        "4. 최근 2주 동안, 거의 매일 피곤하거나 기운이 없었습니까?",
        "5. 최근 2주 동안, 거의 매일 식욕 변화가 있었습니까? (식욕 감소 또는 과식) ",
        "6. 최근 2주 동안, 거의 매일 자신이 실패자라고 느꼈거나, 스스로를 비난했습니까?",
        "7. 최근 2주 동안, 거의 매일 집중하는 것이 어려웠습니까? (예: TV 시청, 독서, 일 수행) ",
        "8. 최근 2주 동안, 거의 매일 움직임이 느려지거나 초조함을 느꼈습니까? (다른 사람이 이를 알아챌 정도로) ",
        "9. 최근 2주 동안, 거의 매일 스스로를 해칠 생각을 했거나, 죽고 싶다는 생각을 했습니까?"
    ]
    
    scores = {"없음": 0, "가끔": 1, "자주": 2, "거의 매일": 3}
    total_score = 0
    
    print("우울증 자가 진단 테스트 (PHQ-9 기반)")
    print("각 질문에 대해 해당하는 번호를 입력하세요.\n")
    print("0: 없음, 1: 가끔 (며칠 동안), 2: 자주 (일주일 이상), 3: 거의 매일\n")
    
    for question in questions:
        while True:
            try:
                answer = int(input(f"{question} (0-3): "))
                if answer in scores.values():
                    total_score += answer
                    break
                else:
                    print("잘못된 입력입니다. 0에서 3 사이의 숫자를 입력하세요.")
            except ValueError:
                print("숫자를 입력하세요.")
    
    print("\n총 점수:", total_score)
    
    if total_score <= 4:
        print("정상 범위입니다. 하지만 기분이 지속적으로 좋지 않다면 전문가 상담을 고려하세요.")
    elif total_score <= 9:
        print("경미한 우울 증상이 의심됩니다. 필요하면 전문가 상담을 받아보세요.")
    elif total_score <= 14:
        print("중등도 우울증이 의심됩니다. 상담 및 치료가 필요할 수 있습니다.")
    elif total_score <= 19:
        print("중증 우울증이 의심됩니다. 전문가의 도움이 필요할 수 있습니다. 상담을 추천합니다.")
    else:
        print("매우 심각한 우울증 상태일 가능성이 높습니다. 즉시 정신건강 전문가와 상담하세요.")

if __name__ == "__main__":
    depression_test()